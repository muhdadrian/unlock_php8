<?php

// The while loop is used when you want the block of code to run while a certain condition is true.

$i = 0;
while ($i < 5) {
    echo $i;
    $i++;
}

// In this example, the variable $i is initialized to 0 before the loop. The block of code will be repeated as long as the condition $i < 5 is true. With each iteration, the value of $i is incremented by 1.