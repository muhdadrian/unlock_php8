# Control structures

# If/else

$weather = "sunny";
//Using if and else
if ($weather == "sunny") {
    echo "Let's go to the beach!";
} else {
    echo "Let's stay home.";
}

// In the example above, we used the $weather variable to represent the weather. With the if and else structure, we check if the weather is sunny and, if true, we display the message Let's go to the beach!. Otherwise, we display the message Let's stay home.