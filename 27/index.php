# Operator precedence

// As with mathematics, PHP follows a specific order of operation, also known as operator precedence. If an expression contains more than one operator, operator precedence determines how those operators are grouped.

// An example of the precedence is as follows:

echo 1 + 2 *3; //Output: 7, not 9, because multiplication has a higher precedence than addition.

# Developer quick tip:

// In mathematics, the standard order of calculation follows the following operator precedence:

# Parentheses: Any operation inside parentheses is performed first.

# Exponentiation: Exponentiation operations, such as raising a number to a power.

# Multiplication and division: Multiplication and division operations are performed from left to right.

# Addition and subtraction: Addition and subtraction operations are also performed from left to right
