# Casting syntax: Another way to perform type conversion is by using casting syntax. Casting is a more concise way of converting a value to a specific type. It is carried out by placing the desired type in parentheses before the value we want to convert. Here is an example:

$number = "123";
$number = (int) $number;  // Convert the string to an integer using casting
echo $number;  // Outputs: 123

// In this example, we use casting (int) syntax to convert the string 123 to an integer. The result is assigned to the $number variable and then printed to the screen.

// Both approaches (type conversion functions and casting syntax ) are valid and effective for performing type conversion. Choosing between them depends on your personal preferences and the context in which you are working.

