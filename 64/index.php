# Search in strings

// The strstr() function is used to search for a substring within a string. An example is as follows:

echo strstr('Hello World!', 'World'); //returns 'World!'

// The strstr() function looks for the sub-string World in the string Hello, World!. Since World is found in the string, the function returns the part of the string from the first character of World to the end, that is, World!.